int	ft_sqrt(int nb)
{
	int i;
	int aux;

	if (nb < 0)
		return (0);
	if (nb <= 1)
		return (nb);
	i = 2;
	aux = 0;
	while (aux < nb)
	{
		aux = i * i;
		if (aux == nb)
			return (i);
		i++;
	}
	return (0);
}
