#include "ft_bsq.h"

int		get_map_height(char *map)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (map[i] != '\0')
	{
		while (map[i] != '\n')
			i++;
		i++;
		j++;
	}
	return (j - 1);
}
