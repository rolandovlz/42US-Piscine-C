char	rot(char c)
{
	if (c >= 65 && c <= 90)
	{
		c -= 65;
		c = (c + 42) % 26;
		c += 65;
		return (c);
	}
	else if (c >= 97 && c <= 122)
	{
		c -= 97;
		c = (c + 42) % 26;
		c += 97;
		return (c);
	}
	else
		return (c);
}

char	*ft_rot42(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		str[i] = rot(str[i]);
		i++;
	}
	return (str);
}
